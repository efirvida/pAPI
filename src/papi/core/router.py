class APIRoute(FastAPIRoute):
    """
    Extended FastAPI route class that adds support for exposing routes as MCP tools.

    Parameters:
    -----------
    is_mcp_tool : bool
        If True, marks this route as part of the Model Context Protocol (MCP) toolset.
        This flag can be used later for filtering or special treatment of these routes.
    """

    is_mcp_tool: bool  # Optional type annotation for better tooling support

